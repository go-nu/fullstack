package a0313;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        int sum = a + b;
        int p = a - b;
        int m = a * b;
        int d = a / b;
        int n = a % b;

        System.out.println("a + b = " + sum);
        System.out.println("a - b = " + p);
        System.out.println("a * b = " + m);
        System.out.println("a / b = " + d);
        System.out.println("a % b = " + n);
    }
}
